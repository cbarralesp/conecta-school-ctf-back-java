UPDATE "CURSOS"
SET "NOMBRE" = TRIM(REGEXP_REPLACE("NOMBRE", '\s+' || "LETRA" || '$', ''))
WHERE COALESCE("LETRA", '') <> ''
  AND UPPER("NOMBRE") LIKE '%' || UPPER("LETRA");

