CREATE TABLE IF NOT EXISTS "CHILE_REGIONES" (
    "ID" BIGINT PRIMARY KEY,
    "CODIGO" VARCHAR(10) NOT NULL,
    "NOMBRE" VARCHAR(160) NOT NULL
);

CREATE TABLE IF NOT EXISTS "CHILE_COMUNAS" (
    "ID" BIGINT PRIMARY KEY,
    "REGION_ID" BIGINT NOT NULL REFERENCES "CHILE_REGIONES"("ID") ON DELETE CASCADE,
    "NOMBRE" VARCHAR(160) NOT NULL
);

CREATE INDEX IF NOT EXISTS "IDX_CHILE_COMUNAS_REGION_ID"
    ON "CHILE_COMUNAS" ("REGION_ID");

ALTER TABLE "PERSONAS"
    ADD COLUMN IF NOT EXISTS "REGION_ID" BIGINT REFERENCES "CHILE_REGIONES"("ID"),
    ADD COLUMN IF NOT EXISTS "COMUNA_ID" BIGINT REFERENCES "CHILE_COMUNAS"("ID");

ALTER TABLE "ALUMNOS"
    ADD COLUMN IF NOT EXISTS "REGION_ID" BIGINT REFERENCES "CHILE_REGIONES"("ID"),
    ADD COLUMN IF NOT EXISTS "COMUNA_ID" BIGINT REFERENCES "CHILE_COMUNAS"("ID");

ALTER TABLE "MATRICULAS"
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_REGION_ID" BIGINT REFERENCES "CHILE_REGIONES"("ID"),
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_COMUNA_ID" BIGINT REFERENCES "CHILE_COMUNAS"("ID");
