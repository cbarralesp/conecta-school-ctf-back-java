CREATE TABLE IF NOT EXISTS "ADMIN_USER_MODULE_ACCESS" (
    "ID" BIGSERIAL PRIMARY KEY,
    "USUARIO_ID" BIGINT NOT NULL REFERENCES "USUARIOS"("ID"),
    "MODULO_CODIGO" VARCHAR(120) NOT NULL,
    "NIVEL_ACCESO" VARCHAR(30) NOT NULL,
    "ACTIVO" BOOLEAN NOT NULL DEFAULT TRUE,
    "CREADO_AT" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    "ACTUALIZADO_AT" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE UNIQUE INDEX IF NOT EXISTS "UX_ADMIN_USER_MODULE_ACCESS_USER_MODULE"
    ON "ADMIN_USER_MODULE_ACCESS" ("USUARIO_ID", "MODULO_CODIGO");
