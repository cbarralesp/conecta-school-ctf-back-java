ALTER TABLE "MATRICULAS"
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_NOMBRE" VARCHAR(160) NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_ANIO_ACADEMICO" VARCHAR(20) NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_DEPENDENCIA" VARCHAR(80) NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_REGION" VARCHAR(120) NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_COMUNA" VARCHAR(120) NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS "ESTABLECIMIENTO_DIRECCION" VARCHAR(255) NOT NULL DEFAULT '';

CREATE TABLE IF NOT EXISTS "MATRICULA_DOCUMENTOS" (
    "ID" BIGSERIAL PRIMARY KEY,
    "MATRICULA_ID" BIGINT NOT NULL REFERENCES "MATRICULAS"("ID") ON DELETE CASCADE,
    "DOCUMENTO_CLAVE" VARCHAR(80) NOT NULL,
    "NOMBRE_ARCHIVO" VARCHAR(255) NOT NULL,
    "DRIVE_FILE_ID" VARCHAR(255),
    "DRIVE_URL" VARCHAR(500),
    "ACTIVO" BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE INDEX IF NOT EXISTS "IDX_MATRICULA_DOCUMENTOS_MATRICULA_ID"
    ON "MATRICULA_DOCUMENTOS" ("MATRICULA_ID");

CREATE UNIQUE INDEX IF NOT EXISTS "UQ_MATRICULA_DOCUMENTOS_CLAVE"
    ON "MATRICULA_DOCUMENTOS" ("MATRICULA_ID", "DOCUMENTO_CLAVE");
