    private JsonNode firstPresent(JsonNode... nodes) {
        for (JsonNode node : nodes) {
            if (node != null && !node.isMissingNode() && !node.isNull()) {
                return node;
            }
        }
        return objectMapper.getNodeFactory().missingNode();
    }

    private String firstText(Object... values) {
        for (Object value : values) {
            String text = null;
            if (value instanceof JsonNode jsonNode) {
                text = jsonNode.isValueNode() ? jsonNode.asText(null) : jsonNode.toString();
            } else if (value instanceof String stringValue) {
                text = stringValue;
            }
            text = nullIfBlank(text);
            if (text != null) {
                return text;
            }
        }
        return null;
    }

    private String firstTextFromArray(JsonNode node) {
        if (node != null && node.isArray() && node.size() > 0) {
            return nullIfBlank(node.get(0).asText(null));
        }
        return null;
    }

    private Integer firstNullableInt(JsonNode... nodes) {
        for (JsonNode node : nodes) {
            Integer value = nullableInt(node);
            if (value != null) {
                return value;
            }
        }
        return null;
    }

    private String resolveOaCode(JsonNode node) {
        if (node == null || node.isMissingNode() || node.isNull()) {
            return null;
        }
        if (node.isValueNode()) {
            return nullIfBlank(node.asText(null));
        }
        return firstText(node.path("codigo"), node.path("oa"), node.path("id"), node.path("codigo_oa"));
    }

    private List<String> resolveOaCodes(JsonNode node) {
        List<String> codes = new ArrayList<>();
        String directCode = resolveOaCode(node);
        if (directCode != null) {
            codes.add(directCode);
        }
        if (node != null && node.isObject()) {
            for (String code : extractTextList(firstPresent(
                    node.path("oas_contexto"),
                    node.path("oas"),
                    node.path("oas_evaluados")
            ))) {
                if (!codes.contains(code)) {
                    codes.add(code);
                }
            }
        }
        return codes;
    }

    private List<String> extractTextList(JsonNode node) {
        List<String> values = new ArrayList<>();
        if (node == null || node.isMissingNode() || node.isNull()) {
            return values;
        }
        if (node.isArray()) {
            for (JsonNode child : node) {
                if (child == null || child.isNull()) {
                    continue;
                }
                String value = child.isObject()
                        ? firstText(child.path("nombre"), child.path("eje"), child.path("descripcion"))
                        : nullIfBlank(child.isValueNode() ? child.asText() : child.toString());
                if (value != null) {
                    values.add(value);
                }
            }
            return values;
        }
        if (node.isObject()) {
            Iterator<Map.Entry<String, JsonNode>> fields = node.fields();
            while (fields.hasNext()) {
                Map.Entry<String, JsonNode> field = fields.next();
                JsonNode child = field.getValue();
                if (child == null || child.isNull()) {
                    continue;
                }
                if (child.isArray()) {
                    values.addAll(extractTextList(child));
                    continue;
                }
                String value = child.isObject()
                        ? firstText(child.path("nombre"), child.path("eje"), child.path("descripcion"), child.path("titulo"))
                        : nullIfBlank(child.asText(null));
                if (value != null) {
                    values.add(value);
                }
            }
            return values;
        }

        String value = nullIfBlank(node.asText(null));
        if (value != null) {
            values.add(value);
        }
        return values;
    }

    private Iterable<JsonNode> iterable(JsonNode node) {
        if (node == null || !node.isArray()) {
            return List.of();
        }
        List<JsonNode> values = new ArrayList<>();
        node.forEach(values::add);
        return values;
    }

    private List<JsonNode> iterableToList(JsonNode node) {
        List<JsonNode> values = new ArrayList<>();
        for (JsonNode value : iterable(node)) {
            values.add(value);
        }
        return values;
    }

    private ReadingValue parseReading(JsonNode readingNode) {
        if (readingNode == null || readingNode.isNull()) {
            return new ReadingValue(null, null);
        }
        if (readingNode.isObject()) {
            return new ReadingValue(
                    nullIfBlank(readingNode.path("titulo").asText(null)),
                    nullIfBlank(readingNode.path("autor").asText(null))
            );
        }

        String rawValue = nullIfBlank(readingNode.asText(null));
        if (rawValue == null) {
            return new ReadingValue(null, null);
        }

        java.util.regex.Matcher matcher = java.util.regex.Pattern.compile("titulo=([^;]+); autor=([^}]+)").matcher(rawValue);
        if (matcher.find()) {
            return new ReadingValue(
                    nullIfBlank(matcher.group(1)),
                    nullIfBlank(matcher.group(2))
            );
        }
        return new ReadingValue(rawValue, null);
    }

    private Integer nullableInt(JsonNode node) {
        if (node == null || node.isMissingNode() || node.isNull()) {
            return null;
        }
        if (node.isInt() || node.isLong()) {
            return node.asInt();
        }
        String value = nullIfBlank(node.asText(null));
        if (value == null) {
            return null;
        }
        try {
            return Integer.parseInt(value.replaceAll("[^0-9\\-]", ""));
        } catch (NumberFormatException exception) {
            return null;
        }
    }

    private String joinTexts(List<String> values) {
        if (values.isEmpty()) {
            return null;
        }
        return String.join("\n", values);
    }

    private String nullIfBlank(String value) {
        if (value == null) {
            return null;
        }
        String trimmed = repairMojibake(value).trim();
        return trimmed.isEmpty() ? null : trimmed;
    }

    private String repairMojibake(String value) {
        if (value == null || (!value.contains("Ã") && !value.contains("Â"))) {
            return value;
        }
        try {
            return new String(value.getBytes(StandardCharsets.ISO_8859_1), StandardCharsets.UTF_8);
        } catch (Exception ignored) {
            return value;
        }
    }

    private String normalize(String value) {
        if (value == null) {
            return "";
        }
        return Normalizer.normalize(value, Normalizer.Form.NFD)
                .replaceAll("\\p{M}", "")
                .replaceAll("\\s+", " ")
                .trim()
                .toLowerCase(Locale.ROOT);
    }

    private String normalizeGrade(String value) {
        return normalize(value)
                .replace("°", "")
                .replace("º", "")
                .replace("ano", "")
                .trim();
    }

