    private ScienceProgramMetadata resolveScienceProgramMetadata(JsonNode root) {
        JsonNode metadata = root.path("metadata");
        if (!metadata.isMissingNode()) {
            return new ScienceProgramMetadata(
                    "Ciencias Naturales",
                    nullIfBlank(metadata.path("nivel").asText(null)),
                    nullIfBlank(metadata.path("decreto").asText(null)),
                    nullIfBlank(metadata.path("ministerio").asText(null)),
                    nullIfBlank(metadata.path("isbn").asText(null)),
                    nullIfBlank(metadata.path("edicion").asText(null)),
                    sumScienceHours(root.path("unidades"))
            );
        }

        return new ScienceProgramMetadata(
                nullIfBlank(root.path("asignatura").asText("Ciencias Naturales")),
                nullIfBlank(root.path("nivel").asText(null)),
                resolveScienceDecree(root),
                nullIfBlank(root.path("fuente").asText(null)),
                null,
                resolveScienceEdition(root),
                sumScienceHours(root.path("unidades"))
        );
    }

    private ScienceProgramMetadata resolveStandardProgramMetadata(JsonNode root, String fallbackSubject) {
        JsonNode general = root.path("datos_generales");
        return new ScienceProgramMetadata(
                firstText(root.path("asignatura"), general.path("asignatura"), fallbackSubject),
                firstText(general.path("grado"), root.path("grado"), root.path("nivel")),
                firstText(root.path("decreto"), general.path("decreto"), resolveScienceDecree(root), extractDecreeFromText(root)),
                firstText(root.path("fuente").path("tipo"), general.path("fuente"), root.path("fuente")),
                firstText(root.path("isbn"), general.path("isbn")),
                firstText(root.path("edicion"), general.path("edicion"), resolveScienceEdition(root), extractEditionFromText(root)),
                firstNullableInt(general.path("horas_total"), root.path("horas_total"), root.path("resumen").path("total_horas")) == null
                        ? sumScienceHours(root.path("unidades"))
                        : firstNullableInt(general.path("horas_total"), root.path("horas_total"), root.path("resumen").path("total_horas"))
        );
    }

    private String extractDecreeFromText(JsonNode root) {
        String text = firstText(root.path("texto_completo_pdf"), root.path("secciones_texto").path("objetivos_y_actitudes_generales"));
        if (text == null) {
            return null;
        }
        java.util.regex.Matcher matcher = java.util.regex.Pattern
                .compile("(D\\.S\\.\\s*\\d+/\\d+|Decreto\\s+Supremo\\s+de\\s+Educaci[oó]n\\s+N[°º]?\\s*\\d+\\s*/\\s*\\d+|Decreto\\s*N[°º]?\\s*\\d+\\s*/\\s*\\d+)", java.util.regex.Pattern.CASE_INSENSITIVE)
                .matcher(text);
        return matcher.find() ? nullIfBlank(matcher.group(1)) : null;
    }

    private String extractEditionFromText(JsonNode root) {
        String text = firstText(root.path("texto_completo_pdf"));
        if (text == null) {
            return null;
        }
        java.util.regex.Matcher matcher = java.util.regex.Pattern
                .compile("(Primera|Segunda|Tercera)\\s+Edici[oó]n\\s*:?\\s*\\d{4}", java.util.regex.Pattern.CASE_INSENSITIVE)
                .matcher(text);
        return matcher.find() ? nullIfBlank(matcher.group(0)) : null;
    }

    private String resolveScienceDecree(JsonNode root) {
        String decree = nullIfBlank(root.path("decreto").asText(null));
        if (decree != null) {
            return decree;
        }
        String source = nullIfBlank(root.path("fuente").asText(null));
        if (source == null) {
            return null;
        }
        java.util.regex.Matcher matcher = java.util.regex.Pattern
                .compile("(D\\.S\\.\\s*\\d+/\\d+|Decreto\\s*(Supremo\\s*de\\s*Educaci[oó]n\\s*)?N[°º]?\\s*\\d+/\\d+|Decreto\\s*N[°º]?\\s*\\d+/\\d+)", java.util.regex.Pattern.CASE_INSENSITIVE)
                .matcher(source);
        return matcher.find() ? nullIfBlank(matcher.group(1)) : null;
    }

    private String resolveScienceEdition(JsonNode root) {
        String source = nullIfBlank(root.path("fuente").asText(null));
        if (source != null && normalize(source).contains("segunda edicion 2018")) {
            return "Segunda Edición 2018";
        }
        return null;
    }

    private Integer sumScienceHours(JsonNode unitsNode) {
        int total = 0;
        boolean hasHours = false;
        for (JsonNode unitNode : iterable(unitsNode)) {
            Integer hours = firstNullableInt(
                    unitNode.path("horas_pedagogicas"),
                    unitNode.path("tiempo_estimado"),
                    unitNode.path("horas_estimadas")
            );
            if (hours != null) {
                total += hours;
                hasHours = true;
            }
        }
        return hasHours ? total : null;
    }

    private JsonNode resolveScienceAxes(JsonNode root) {
        JsonNode thematicAxes = root.path("ejes_tematicos");
        if (!thematicAxes.isMissingNode() && !thematicAxes.isNull()) {
            return thematicAxes;
        }
        JsonNode structuredThematicAxes = root.path("estructura_curricular").path("ejes_tematicos");
        if (!structuredThematicAxes.isMissingNode() && !structuredThematicAxes.isNull()) {
            return structuredThematicAxes;
        }
        return root.path("estructura_curricular").path("ejes");
    }

    private Map<String, ScienceObjectiveMetadata> buildScienceObjectiveMetadata(JsonNode root) {
        Map<String, ScienceObjectiveMetadata> objectives = new LinkedHashMap<>();
        JsonNode catalog = firstPresent(
                root.path("listado_completo_OA"),
                root.path("objetivos_de_aprendizaje"),
                root.path("objetivos_aprendizaje")
        );
        collectScienceObjectives(objectives, catalog, null);

        for (JsonNode unitNode : iterable(root.path("unidades"))) {
            collectScienceObjectives(objectives, firstPresent(
                    unitNode.path("objetivos_aprendizaje"),
                    unitNode.path("objetivos_de_aprendizaje")
            ), null);
        }
        return objectives;
    }

    private void collectScienceObjectives(Map<String, ScienceObjectiveMetadata> objectives, JsonNode node, String fallbackAxis) {
        if (node == null || node.isMissingNode() || node.isNull()) {
            return;
        }
        if (node.isArray()) {
            for (JsonNode objectiveNode : node) {
                addScienceObjectiveMetadata(objectives, objectiveNode, fallbackAxis);
            }
            return;
        }
        if (node.isObject()) {
            Iterator<Map.Entry<String, JsonNode>> fields = node.fields();
            while (fields.hasNext()) {
                Map.Entry<String, JsonNode> field = fields.next();
                JsonNode value = field.getValue();
                String axis = nullIfBlank(field.getKey());
                if (value.isArray()) {
                    collectScienceObjectives(objectives, value, axis);
                } else {
                    addScienceObjectiveMetadata(objectives, value, fallbackAxis);
                }
            }
        }
    }

    private void addScienceObjectiveMetadata(Map<String, ScienceObjectiveMetadata> objectives, JsonNode objectiveNode, String fallbackAxis) {
        String code = resolveOaCode(objectiveNode);
        if (code == null) {
            return;
        }
        String axis = firstText(objectiveNode.path("eje"), objectiveNode.path("habilidad"), fallbackAxis);
        String description = firstText(objectiveNode.path("descripcion"), objectiveNode.path("oa_descripcion"), objectiveNode.path("texto"));
        ScienceObjectiveMetadata existing = objectives.get(code);
        objectives.put(code, new ScienceObjectiveMetadata(
                firstText(axis, existing == null ? null : existing.axis()),
                firstText(description, existing == null ? null : existing.description())
        ));
    }

    private Map<String, Long> insertScienceObjectiveCatalog(Long programId, Map<String, ScienceObjectiveMetadata> objectiveMetadata) {
        Map<String, Long> objectiveIds = new LinkedHashMap<>();
        int order = 1;
        for (Map.Entry<String, ScienceObjectiveMetadata> entry : objectiveMetadata.entrySet()) {
            String code = entry.getKey();
            ScienceObjectiveMetadata metadata = entry.getValue();
            Long objectiveId = jdbcTemplate.queryForObject("""
                    INSERT INTO "PROGRAMAS_ESTUDIO_OAS" (
                        "PROGRAMA_ID",
                        "ORDEN",
                        "CODIGO",
                        "EJE",
                        "DESCRIPCION"
                    )
                    VALUES (?, ?, ?, ?, ?)
                    RETURNING "ID"
                    """, Long.class,
                    programId,
                    order++,
                    code,
                    nullIfBlank(metadata.axis()),
                    firstText(metadata.description(), "Descripción no disponible")
            );
            objectiveIds.put(code, objectiveId);
        }
        return objectiveIds;
    }

    private void insertScienceUnits(
            Long programId,
            JsonNode unitsNode,
            Map<String, Long> objectiveIds,
            Map<String, ScienceObjectiveMetadata> objectiveMetadata
    ) {
        for (JsonNode unitNode : iterable(unitsNode)) {
            Long unitId = insertScienceUnit(programId, unitNode);
            insertFlexibleAttitudes(programId, unitId, firstPresent(
                    unitNode.path("actitudes_trabajadas"),
                    unitNode.path("actitudes"),
                    unitNode.path("actitudes_unidad")
            ));

            Map<String, List<JsonNode>> unitActivitiesByObjective = indexActivitiesByObjective(unitNode.path("actividades"));
            JsonNode objectivesNode = firstPresent(unitNode.path("objetivos_aprendizaje"), unitNode.path("objetivos_de_aprendizaje"));
            int order = 1;
            for (JsonNode objectiveNode : iterable(objectivesNode)) {
                String code = resolveOaCode(objectiveNode);
                if (code == null) {
                    continue;
                }
                ScienceObjectiveMetadata catalogMetadata = objectiveMetadata.get(code);
                String axis = firstText(objectiveNode.path("eje"), objectiveNode.path("habilidad"), catalogMetadata == null ? null : catalogMetadata.axis());
                String description = firstText(
                        objectiveNode.path("descripcion"),
                        catalogMetadata == null ? null : catalogMetadata.description(),
                        "Descripción no disponible"
                );

                Long objectiveId = ensureScienceObjective(programId, objectiveIds, code, axis, description);
                Long unitObjectiveId = insertScienceUnitObjective(programId, unitId, objectiveId, order++, code, axis, description);

                insertIndicators("PROGRAMAS_ESTUDIO_UNIDAD_OA_INDICADORES", "UNIDAD_OA_ID", unitObjectiveId, firstPresent(
                        objectiveNode.path("indicadores_evaluacion"),
                        objectiveNode.path("indicadores_de_evaluacion"),
                        objectiveNode.path("indicadores")
                ));

                JsonNode inlineActivities = objectiveNode.path("actividades");
                if (!inlineActivities.isMissingNode() && !inlineActivities.isNull()) {
                    insertScienceActivities(unitObjectiveId, inlineActivities);
                }
                insertScienceActivities(unitObjectiveId, unitActivitiesByObjective.getOrDefault(code, List.of()));

                insertScienceEvaluations(unitId, code, objectiveNode.path("ejemplos_evaluacion"));
            }

            insertScienceEvaluations(unitId, null, firstPresent(
                    unitNode.path("ejemplos_de_evaluacion"),
                    unitNode.path("ejemplos_evaluacion"),
                    unitNode.path("evaluaciones")
            ));
        }
    }

