    private void seedStandardProgram(String programCode, String classpathLocation, String fallbackSubject, boolean includePermanentObjectives) throws Exception {
        ClassPathResource resource = new ClassPathResource(classpathLocation);
        if (!resource.exists()) {
            LOGGER.warn("No se encontro recurso de {} en {}", fallbackSubject, classpathLocation);
            return;
        }

        String rawJson;
        try (InputStream inputStream = resource.getInputStream()) {
            rawJson = new String(inputStream.readAllBytes(), StandardCharsets.UTF_8);
        }

        JsonNode root = objectMapper.readTree(rawJson);
        ScienceProgramMetadata metadata = resolveStandardProgramMetadata(root, fallbackSubject);
        Map<String, ScienceObjectiveMetadata> objectiveMetadata = buildScienceObjectiveMetadata(root);
        Integer totalObjectives = firstNullableInt(
                root.path("estructura_curricular").path("total_oa"),
                root.path("resumen").path("total_oas_unicos")
        );

        Long programId = upsertProgram(
                programCode,
                metadata.subject(),
                metadata.grade(),
                metadata.decree(),
                metadata.source(),
                metadata.isbn(),
                metadata.edition(),
                firstNullableInt(
                        root.path("datos_generales").path("total_unidades"),
                        root.path("resumen").path("total_unidades")
                ),
                totalObjectives == null ? objectiveMetadata.size() : totalObjectives,
                metadata.totalHours(),
                null,
                rawJson
        );
        clearProgramChildren(programId);

        insertAxes(programId, firstPresent(
                root.path("estructura_curricular").path("ejes"),
                root.path("estructura_curricular").path("habilidades"),
                root.path("habilidades")
        ));
        insertFlexibleAttitudes(programId, null, root.path("actitudes"));

        Map<String, Long> objectiveIds = insertScienceObjectiveCatalog(programId, objectiveMetadata);
        if (includePermanentObjectives) {
            insertStandardPermanentObjectives(programId, root.path("oas_todas_las_unidades"), objectiveIds, objectiveMetadata);
        }
        insertScienceUnits(programId, root.path("unidades"), objectiveIds, objectiveMetadata);
    }

