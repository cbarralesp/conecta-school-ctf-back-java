    private void seedStudyProgram(String programCode, String classpathLocation) throws Exception {
        ClassPathResource resource = new ClassPathResource(classpathLocation);
        if (!resource.exists()) {
            LOGGER.warn("No se encontro recurso de Programa de Estudio en {}", classpathLocation);
            return;
        }

        String rawJson;
        try (InputStream inputStream = resource.getInputStream()) {
            rawJson = new String(inputStream.readAllBytes(), StandardCharsets.UTF_8);
        }

        JsonNode root = objectMapper.readTree(rawJson);
        JsonNode document = root.path("documento");

        Long programId = upsertProgram(programCode, document, root.path("oas_todas_las_unidades").path("descripcion").asText(null), rawJson);
        clearProgramChildren(programId);

        insertAxes(programId, document.path("ejes"));
        insertGlobalAttitudes(programId, root.path("actitudes"));

        Map<String, Long> objectiveIds = insertObjectiveCatalog(programId, root.path("objetivos_de_aprendizaje"));
        insertPermanentObjectives(programId, root.path("oas_todas_las_unidades"), objectiveIds);
        insertUnits(programId, root.path("unidades"), objectiveIds);
    }

