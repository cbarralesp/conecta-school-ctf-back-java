    private Map<String, List<JsonNode>> indexActivitiesByObjective(JsonNode activitiesNode) {
        Map<String, List<JsonNode>> activitiesByObjective = new LinkedHashMap<>();
        for (JsonNode activityNode : iterable(activitiesNode)) {
            List<String> codes = resolveOaCodes(activityNode);
            if (codes.isEmpty()) {
                continue;
            }
            for (String code : codes) {
                activitiesByObjective.computeIfAbsent(code, ignored -> new ArrayList<>()).add(activityNode);
            }
        }
        return activitiesByObjective;
    }

