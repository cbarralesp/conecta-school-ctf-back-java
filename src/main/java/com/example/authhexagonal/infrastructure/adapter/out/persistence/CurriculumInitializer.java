    private static final Logger LOGGER = LoggerFactory.getLogger(CurriculumInitializer.class);

    private final JdbcTemplate jdbcTemplate;

    public CurriculumInitializer(JdbcTemplate jdbcTemplate) {
        this.jdbcTemplate = jdbcTemplate;
    }

    @Override
    public void run(String... args) {
        createSchema();
        seedNicoleFirstGradePilot();
        seedFirstGradeObjectives();
        LOGGER.info("Catalogo curricular base verificado para planificaciones");
    }

    private void createSchema() {
        jdbcTemplate.execute("""
                CREATE TABLE IF NOT EXISTS "CURRICULUM_OBJETIVOS" (
                    "ID" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    "ASIGNATURA_ID" BIGINT NOT NULL,
                    "CURSO_ID" BIGINT NOT NULL,
                    "UNIDAD_NUMERO" VARCHAR(30) NOT NULL DEFAULT 'GENERAL',
                    "CODIGO" VARCHAR(30) NOT NULL,
                    "TITULO" VARCHAR(180) NOT NULL,
                    "DESCRIPCION" TEXT NOT NULL,
                    "EJE" VARCHAR(140),
                    "FUENTE" VARCHAR(220) NOT NULL DEFAULT 'Bases curriculares',
                    "ACTIVO" BOOLEAN NOT NULL DEFAULT TRUE,
                    "FECHA_CREACION" TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
                    CONSTRAINT "FK_CURRICULUM_OBJETIVOS_ASIGNATURA"
                        FOREIGN KEY ("ASIGNATURA_ID") REFERENCES "ASIGNATURAS" ("ID"),
                    CONSTRAINT "FK_CURRICULUM_OBJETIVOS_CURSO"
                        FOREIGN KEY ("CURSO_ID") REFERENCES "CURSOS" ("ID"),
                    CONSTRAINT "UK_CURRICULUM_OBJETIVO"
                        UNIQUE ("ASIGNATURA_ID", "CURSO_ID", "UNIDAD_NUMERO", "CODIGO")
                )
                """);

        jdbcTemplate.execute("""
                CREATE TABLE IF NOT EXISTS "CURRICULUM_OBJETIVO_HABILIDADES" (
                    "ID" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    "OBJETIVO_ID" BIGINT NOT NULL,
                    "NOMBRE" VARCHAR(120) NOT NULL,
                    CONSTRAINT "FK_CURRICULUM_HABILIDAD_OBJETIVO"
                        FOREIGN KEY ("OBJETIVO_ID") REFERENCES "CURRICULUM_OBJETIVOS" ("ID") ON DELETE CASCADE,
                    CONSTRAINT "UK_CURRICULUM_HABILIDAD"
                        UNIQUE ("OBJETIVO_ID", "NOMBRE")
                )
                """);

        jdbcTemplate.execute("""
                CREATE TABLE IF NOT EXISTS "CURRICULUM_OBJETIVO_ACTITUDES" (
                    "ID" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    "OBJETIVO_ID" BIGINT NOT NULL,
                    "DESCRIPCION" VARCHAR(260) NOT NULL,
                    CONSTRAINT "FK_CURRICULUM_ACTITUD_OBJETIVO"
                        FOREIGN KEY ("OBJETIVO_ID") REFERENCES "CURRICULUM_OBJETIVOS" ("ID") ON DELETE CASCADE,
                    CONSTRAINT "UK_CURRICULUM_ACTITUD"
                        UNIQUE ("OBJETIVO_ID", "DESCRIPCION")
                )
                """);

        jdbcTemplate.execute("""
                CREATE INDEX IF NOT EXISTS "IDX_CURRICULUM_OBJETIVOS_ASIG_CURSO"
                    ON "CURRICULUM_OBJETIVOS" ("ASIGNATURA_ID", "CURSO_ID")
                """);
    }

