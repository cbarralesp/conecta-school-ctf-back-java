    private void deactivateFirstGradeSubjectObjectives(String subjectPattern) {
        jdbcTemplate.update("""
                UPDATE "CURRICULUM_OBJETIVOS" co
                SET "ACTIVO" = FALSE
                FROM "ASIGNATURAS" a, "CURSOS" c
                WHERE co."ASIGNATURA_ID" = a."ID"
                  AND co."CURSO_ID" = c."ID"
                  AND a."NOMBRE" ILIKE ?
                  AND (
                    c."NOMBRE" ILIKE '1%Basico%'
                    OR c."NOMBRE" ILIKE '1%Básico%'
                    OR c."CODIGO" ILIKE '1%'
                  )
                """, subjectPattern);
    }

    private void seedNicoleFirstGradePilot() {
        ensureCoreSubject("ART", "Artes", "Artes", "#E4E3FA", "Creacion artistica y apreciacion visual.");
        ensureCoreSubject("MAT", "Matematica", "Ciencias basicas", "#D7E8FB", "Pensamiento numerico y resolucion de problemas.");
        ensureCoreSubject("LEN", "Lenguaje", "Comunicacion", "#F4E1D9", "Lectura, escritura y comunicacion oral.");
        ensureFirstGradeCourse();

        List<Long> nicoleTeacherIds = jdbcTemplate.query("""
                SELECT pr."ID"
                FROM "PROFESORES" pr
                JOIN "PERSONAS" pe ON pe."ID" = pr."PERSONA_ID"
                LEFT JOIN "USUARIOS" u ON u."PERSONA_ID" = pe."ID"
                WHERE UPPER(pe."NOMBRES" || ' ' || pe."APELLIDOS") LIKE '%NICOLE%'
                   OR UPPER(pe."CORREO_ELECTRONICO") LIKE '%NICOLE%'
                   OR pr."CODIGO" = 'PROF-006'
                   OR u."USUARIO" = 'nramirez'
                ORDER BY pr."ID"
                """, (rs, rowNum) -> rs.getLong("ID"));

        if (nicoleTeacherIds.isEmpty()) {
            LOGGER.warn("No se encontro profesora Nicole para asociar piloto curricular de 1 Basico");
            return;
        }

        for (Long nicoleTeacherId : nicoleTeacherIds) {
            for (SubjectSeed subject : List.of(
                    new SubjectSeed("ART", 2, "Exploracion visual y expresion artistica"),
                    new SubjectSeed("MAT", 6, "Numeros, conteo y representacion inicial"),
                    new SubjectSeed("LEN", 6, "Comprension oral, lectura inicial y comunicacion")
            )) {
                upsertTeacherSubject(nicoleTeacherId, subject.code());
                Long loadId = upsertTeacherLoad(nicoleTeacherId, subject.code(), subject.weeklyHours());
                upsertPlanningUnit(
                        loadId,
                        nicoleTeacherId,
                        subject.name(),
                        subject.code()
                );
            }
        }
    }

    private void ensureCoreSubject(
            String code,
            String name,
            String area,
            String color,
            String description
    ) {
        jdbcTemplate.update("""
                INSERT INTO "ASIGNATURAS" (
                    "CODIGO",
                    "NOMBRE",
                    "AREA",
                    "COLOR_HEX",
                    "ACTIVA",
                    "DESCRIPCION",
                    "NIVEL_REFERENCIA",
                    "HORAS_SUGERIDAS"
                )
                VALUES (?, ?, ?, ?, TRUE, ?, 'Ensenanza basica', 2)
                ON CONFLICT ("CODIGO") DO UPDATE
                SET "ACTIVA" = TRUE
                """, code, name, area, color, description);
    }

    private void ensureFirstGradeCourse() {
        jdbcTemplate.update("""
                INSERT INTO "CURSOS" (
                    "CODIGO",
                    "NOMBRE",
                    "NIVEL",
                    "LETRA",
                    "ANIO_ESCOLAR",
                    "JORNADA",
                    "ACTIVO"
                )
                VALUES ('1A-2026', '1 Basico A', '1 Basico', 'A', 2026, 'Manana', TRUE)
                ON CONFLICT ("CODIGO") DO UPDATE
                SET "NOMBRE" = EXCLUDED."NOMBRE",
                    "NIVEL" = EXCLUDED."NIVEL",
                    "LETRA" = EXCLUDED."LETRA",
                    "ANIO_ESCOLAR" = EXCLUDED."ANIO_ESCOLAR",
                    "JORNADA" = EXCLUDED."JORNADA",
                    "ACTIVO" = TRUE
                """);
    }

    private void upsertTeacherSubject(Long teacherId, String subjectCode) {
        if (columnExists("PROFESOR_ASIGNATURAS", "ASIGNATURA")) {
            jdbcTemplate.update("""
                    INSERT INTO "PROFESOR_ASIGNATURAS" ("PROFESOR_ID", "ASIGNATURA_ID", "ASIGNATURA", "ACTIVO")
                    SELECT ?, a."ID", a."NOMBRE", TRUE
                    FROM "ASIGNATURAS" a
                    WHERE a."CODIGO" = ?
                      AND NOT EXISTS (
                        SELECT 1
                        FROM "PROFESOR_ASIGNATURAS" pa
                        WHERE pa."PROFESOR_ID" = ?
                          AND (
                            pa."ASIGNATURA_ID" = a."ID"
                            OR UPPER(TRIM(pa."ASIGNATURA")) = UPPER(TRIM(a."NOMBRE"))
                          )
                      )
                    """, teacherId, subjectCode, teacherId);
            return;
        }

        jdbcTemplate.update("""
                    INSERT INTO "PROFESOR_ASIGNATURAS" ("PROFESOR_ID", "ASIGNATURA_ID", "ACTIVO")
                    SELECT ?, a."ID", TRUE
                    FROM "ASIGNATURAS" a
                    WHERE a."CODIGO" = ?
                      AND NOT EXISTS (
                        SELECT 1
                        FROM "PROFESOR_ASIGNATURAS" pa
                        WHERE pa."PROFESOR_ID" = ?
                          AND pa."ASIGNATURA_ID" = a."ID"
                      )
                    """, teacherId, subjectCode, teacherId);
    }

    private Long upsertTeacherLoad(Long teacherId, String subjectCode, int weeklyHours) {
        List<Long> existing = jdbcTemplate.query("""
                SELECT cd."ID"
                FROM "CARGAS_DOCENTES" cd
                JOIN "CURSOS" c ON c."ID" = cd."CURSO_ID"
                JOIN "ASIGNATURAS" a ON a."ID" = cd."ASIGNATURA_ID"
                WHERE cd."PROFESOR_ID" = ?
                  AND c."CODIGO" = '1A-2026'
                  AND a."CODIGO" = ?
                  AND cd."ANIO_ESCOLAR" = 2026
                """, (rs, rowNum) -> rs.getLong("ID"), teacherId, subjectCode);

        if (!existing.isEmpty()) {
            Long loadId = existing.get(0);
            jdbcTemplate.update("""
                    UPDATE "CARGAS_DOCENTES"
                    SET "ACTIVA" = TRUE,
                        "HORAS_SEMANALES" = ?
                    WHERE "ID" = ?
                    """, weeklyHours, loadId);
            return loadId;
        }

        return jdbcTemplate.queryForObject("""
                INSERT INTO "CARGAS_DOCENTES" (
                    "PROFESOR_ID",
                    "CURSO_ID",
                    "ASIGNATURA_ID",
                    "ANIO_ESCOLAR",
                    "HORAS_SEMANALES",
                    "ES_PROFESOR_JEFE",
                    "ACTIVA"
                )
                SELECT ?, c."ID", a."ID", 2026, ?, FALSE, TRUE
                FROM "CURSOS" c
                JOIN "ASIGNATURAS" a ON a."CODIGO" = ?
                WHERE c."CODIGO" = '1A-2026'
                RETURNING "ID"
                """, Long.class, teacherId, weeklyHours, subjectCode);
    }

    private void upsertPlanningUnit(Long loadId, Long teacherId, String subjectName, String subjectCode) {
        Long userId = jdbcTemplate.query("""
                SELECT u."ID"
                FROM "USUARIOS" u
                JOIN "PROFESORES" pr ON pr."PERSONA_ID" = u."PERSONA_ID"
                WHERE pr."ID" = ?
                ORDER BY u."ID"
                """, (rs, rowNum) -> rs.getLong("ID"), teacherId).stream().findFirst().orElse(null);

        if (userId == null) {
            LOGGER.warn("Nicole no tiene usuario asociado; no se creara unidad piloto para {}", subjectCode);
            return;
        }

        jdbcTemplate.update("""
                INSERT INTO "UNIDADES_PLANIFICACION" (
                    "ID",
                    "CARGA_DOCENTE_ID",
                    "NUMERO_UNIDAD",
                    "NOMBRE",
                    "SEMANA_INICIO",
                    "FECHA_INICIO",
                    "FECHA_TERMINO",
                    "SEMANAS_ESTIMADAS",
                    "CLASES_PLANIFICADAS",
                    "DESCRIPCION_GENERAL",
                    "OBJETIVOS_APRENDIZAJE",
                    "INDICADORES_LOGRO",
                    "ESTADO",
                    "CREADO_POR_USUARIO_ID"
                )
                SELECT
                    ?,
                    ?,
                    'UNIDAD_I',
                    ?,
                    1,
                    DATE '2026-03-02',
                    DATE '2026-04-10',
                    6,
                    6,
                    ?,
                    ?,
                    ?,
                    'ACTIVA',
                    ?
                WHERE NOT EXISTS (
                    SELECT 1
                    FROM "UNIDADES_PLANIFICACION"
                    WHERE "CARGA_DOCENTE_ID" = ?
                      AND "NUMERO_UNIDAD" = 'UNIDAD_I'
                )
                """,
                nextTableId("UNIDADES_PLANIFICACION"),
                loadId,
                "Bases curriculares 1 Basico - " + subjectName,
                "Unidad piloto para validar objetivos de aprendizaje desde bases curriculares.",
                "Objetivos curriculares iniciales de " + subjectName + " para 1 Basico.",
                "Selecciona OA del catalogo curricular para estructurar la clase.",
                userId,
                loadId
        );
    }

    private Long nextTableId(String tableName) {
        Long nextId = jdbcTemplate.queryForObject(
                "SELECT COALESCE(MAX(\"ID\"), 0) + 1 FROM \"" + tableName + "\"",
                Long.class
        );
        return nextId == null ? 1L : nextId;
    }

    private void upsertObjectiveForFirstGrade(
            String subjectPattern,
            String code,
            String title,
            String description,
            String axis,
            List<String> skills,
            List<String> attitudes
    ) {
        jdbcTemplate.update("""
                INSERT INTO "CURRICULUM_OBJETIVOS" (
                    "ASIGNATURA_ID",
                    "CURSO_ID",
                    "UNIDAD_NUMERO",
                    "CODIGO",
                    "TITULO",
                    "DESCRIPCION",
                    "EJE",
                    "FUENTE",
                    "ACTIVO"
                )
                SELECT
                    a."ID",
                    c."ID",
                    'GENERAL',
                    ?,
                    ?,
                    ?,
                    ?,
                    'Bases curriculares - carga inicial controlada',
                    TRUE
                FROM "ASIGNATURAS" a
                CROSS JOIN "CURSOS" c
                WHERE a."ACTIVA" = TRUE
                  AND c."ACTIVO" = TRUE
                  AND a."NOMBRE" ILIKE ?
                  AND (
                    c."NOMBRE" ILIKE '1%Basico%'
                    OR c."NOMBRE" ILIKE '1%Básico%'
                    OR c."CODIGO" ILIKE '1%'
                  )
                ON CONFLICT ("ASIGNATURA_ID", "CURSO_ID", "UNIDAD_NUMERO", "CODIGO") DO UPDATE
                SET "TITULO" = EXCLUDED."TITULO",
                    "DESCRIPCION" = EXCLUDED."DESCRIPCION",
                    "EJE" = EXCLUDED."EJE",
                    "ACTIVO" = TRUE
                """, code, title, description, axis, subjectPattern);

        List<Long> objectiveIds = jdbcTemplate.query("""
                SELECT co."ID"
                FROM "CURRICULUM_OBJETIVOS" co
                JOIN "ASIGNATURAS" a ON a."ID" = co."ASIGNATURA_ID"
                JOIN "CURSOS" c ON c."ID" = co."CURSO_ID"
                WHERE a."NOMBRE" ILIKE ?
                  AND co."CODIGO" = ?
                  AND (
                    c."NOMBRE" ILIKE '1%Basico%'
                    OR c."NOMBRE" ILIKE '1%Básico%'
                    OR c."CODIGO" ILIKE '1%'
                  )
                """, (rs, rowNum) -> rs.getLong("ID"), subjectPattern, code);

        for (Long objectiveId : objectiveIds) {
            for (String skill : skills) {
                jdbcTemplate.update("""
                        INSERT INTO "CURRICULUM_OBJETIVO_HABILIDADES" ("OBJETIVO_ID", "NOMBRE")
                        VALUES (?, ?)
                        ON CONFLICT ("OBJETIVO_ID", "NOMBRE") DO NOTHING
                        """, objectiveId, skill);
            }

            for (String attitude : attitudes) {
                jdbcTemplate.update("""
                        INSERT INTO "CURRICULUM_OBJETIVO_ACTITUDES" ("OBJETIVO_ID", "DESCRIPCION")
                        VALUES (?, ?)
                        ON CONFLICT ("OBJETIVO_ID", "DESCRIPCION") DO NOTHING
                        """, objectiveId, attitude);
            }
        }
    }

    private record SubjectSeed(String code, int weeklyHours, String name) {
    }

    private boolean columnExists(String tableName, String columnName) {
        Boolean exists = jdbcTemplate.queryForObject("""
                SELECT EXISTS (
                    SELECT 1
                    FROM information_schema.columns
                    WHERE table_schema = 'public'
                      AND (table_name = ? OR table_name = LOWER(?) OR table_name = UPPER(?))
                      AND (column_name = ? OR column_name = LOWER(?) OR column_name = UPPER(?))
                )
                """, Boolean.class, tableName, tableName, tableName, columnName, columnName, columnName);
        return Boolean.TRUE.equals(exists);
    }
