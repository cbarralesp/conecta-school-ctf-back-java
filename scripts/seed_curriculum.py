from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any


def fix_text(value: Any) -> str:
    if value is None:
        return ""
    text = str(value)
    try:
        repaired = text.encode("latin-1").decode("utf-8")
        mojibake_tokens = ("Ã", "Â", "â", "ð")
        if any(token in text for token in mojibake_tokens) and not any(token in repaired for token in mojibake_tokens):
            return repaired
        return repaired if repaired.count("�") < text.count("�") else text
    except (UnicodeEncodeError, UnicodeDecodeError):
        return text


def sql_quote(value: Any) -> str:
    text = fix_text(value)
    return "'" + text.replace("'", "''") + "'"


def build_statements(payload: dict[str, Any]) -> list[str]:
    statements: list[str] = [
        "-- Auto-generated by scripts/seed_curriculum.py",
        "BEGIN;",
    ]

    for subject in payload.get("asignaturas", []):
        statements.append(
            "INSERT INTO curriculum_subjects (id, slug, nombre, total_grados) VALUES "
            f"('{subject['id']}', {sql_quote(subject['slug'])}, {sql_quote(subject['nombre'])}, {int(subject['total_grados'])}) "
            "ON CONFLICT (id) DO UPDATE SET "
            "slug = EXCLUDED.slug, "
            "nombre = EXCLUDED.nombre, "
            "total_grados = EXCLUDED.total_grados;"
        )

        for grade in subject.get("grados", []):
            statements.append(
                "INSERT INTO curriculum_grades (id, subject_id, grado, label, total_objetivos) VALUES "
                f"('{grade['id']}', '{subject['id']}', {sql_quote(grade['grado'])}, {sql_quote(grade['label'])}, {int(grade['total_objetivos'])}) "
                "ON CONFLICT (id) DO UPDATE SET "
                "subject_id = EXCLUDED.subject_id, "
                "grado = EXCLUDED.grado, "
                "label = EXCLUDED.label, "
                "total_objetivos = EXCLUDED.total_objetivos;"
            )

            for objective in grade.get("objetivos_de_aprendizaje", []):
                statements.append(
                    "INSERT INTO curriculum_objectives (id, grade_id, codigo, tipo, eje, descripcion) VALUES "
                    f"('{objective['id']}', '{grade['id']}', {sql_quote(objective['codigo'])}, {sql_quote(objective['tipo'])}, "
                    f"{sql_quote(objective.get('eje', ''))}, {sql_quote(objective['descripcion'])}) "
                    "ON CONFLICT (id) DO UPDATE SET "
                    "grade_id = EXCLUDED.grade_id, "
                    "codigo = EXCLUDED.codigo, "
                    "tipo = EXCLUDED.tipo, "
                    "eje = EXCLUDED.eje, "
                    "descripcion = EXCLUDED.descripcion;"
                )

                for index, item in enumerate(objective.get("sub_items", []), start=1):
                    statements.append(
                        "INSERT INTO curriculum_objective_items (objective_id, orden, descripcion) VALUES "
                        f"('{objective['id']}', {index}, {sql_quote(item)}) "
                        "ON CONFLICT (objective_id, orden) DO UPDATE SET "
                        "descripcion = EXCLUDED.descripcion;"
                    )

    statements.append("COMMIT;")
    return statements


def main() -> int:
    if len(sys.argv) != 2:
        print("Usage: py scripts/seed_curriculum.py <json-path>", file=sys.stderr)
        return 1

    json_path = Path(sys.argv[1])
    if not json_path.exists():
        print(f"JSON file not found: {json_path}", file=sys.stderr)
        return 1

    payload = json.loads(json_path.read_text(encoding="utf-8"))
    statements = build_statements(payload)
    output = "\n".join(statements) + "\n"
    sys.stdout.buffer.write(output.encode("utf-8"))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
